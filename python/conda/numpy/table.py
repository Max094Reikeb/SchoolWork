import numpy as np

# Créer un tableau à partir d'une liste
arr = np.array([1, 2, 3, 4, 5])

# Créer un tableau de zéros
zeros = np.zeros(5)

# Créer un tableau de uns
ones = np.ones(5)

# Créer un tableau d'identité
identity = np.eye(3)

print("Tableau ", arr)
print("Zéros ", zeros)
print("Uns ", ones)
print("Identité ", identity)
